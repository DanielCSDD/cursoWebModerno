
// É um objeto da API de Javascript, que tem muitas utilidades.
const raio = 5.6

// Math.pow(valor do raio, valor do expoente)
const area = Math.PI * Math.pow(raio, 2)

// Imprimindo o valor de area
console.log(area)

// Corrigindo as casas decimais
console.log(area.toFixed(2))

// Verificando o tipo da variável area.
console.log(typeof area)