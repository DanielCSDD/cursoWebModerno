// Tipagem fraca é um conceito, onde o tipo de dado que uma variável pode suportar é flexível.
// Por exemplo, a = 1 (a variável a neste moento é do tipo inteiro), a = 'casa' (neste outro exemplo,
// ela passa a ser string). Ou seja, tipagem fraca possibilita maior flexibilidade ao atribuir dados as variável.
let qualquer = 'Legal'

// Imprimindo o conteúdo da variável: qualquer.
console.log(qualquer)

// Imprimindo o tipo de dados que a variável possuí.
console.log(typeof qualquer)